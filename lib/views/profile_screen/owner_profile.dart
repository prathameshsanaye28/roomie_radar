class OwnerProfile {
  final String name;
  final String email;
  final String dob;
  final String gender;
  final String contact;
  final String address;
  final String imageUrl;

  OwnerProfile({
    required this.name,
    required this.email,
    required this.dob,
    required this.gender,
    required this.contact,
    required this.address,
    required this.imageUrl,
  });
}