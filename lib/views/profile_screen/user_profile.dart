// user_profile.dart
class UserProfile {
  final String name;
  final String username;
  final String email;
  final String dob;
  final String gender;
  final String contact;
  final String address;
  final String imageUrl;

  UserProfile({
    required this.name,
    required this.username,
    required this.email,
    required this.dob,
    required this.gender,
    required this.contact,
    required this.address,
    required this.imageUrl,
  });
}
